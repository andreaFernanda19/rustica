package Clases;

public class Boleta {
    private int cantidad;
    private String bebidas;
    private String tequeños;
    private String pasta;
    private String peruanitos;
    private String pizza;
    private String pollo;
    private String parrilla;  
    private String formadepago;
    private double importetotal;

    public int getCantidad() {
        return cantidad;
    }
    public void setCantidad(int cantidad) {
        this.cantidad = cantidad;
    }
    public String getBebidas() {
        return bebidas;
    }

    public void setBebidas(String bebidas) {
        this.bebidas = bebidas;
    }

    public String getTequeños() {
        return tequeños;
    }

    public void setTequeños(String tequeños) {
        this.tequeños = tequeños;
    }

    public String getPasta() {
        return pasta;
    }

    public void setPasta(String pasta) {
        this.pasta = pasta;
    }

    public String getPeruanitos() {
        return peruanitos;
    }

    public void setPeruanitos(String peruanitos) {
        this.peruanitos = peruanitos;
    }

    public String getPizza() {
        return pizza;
    }

    public void setPizza(String pizza) {
        this.pizza = pizza;
    }

    public String getPollo() {
        return pollo;
    }

    public void setPollo(String pollo) {
        this.pollo = pollo;
    }

    public String getParrilla() {
        return parrilla;
    }

    public void setParrilla(String parrilla) {
        this.parrilla = parrilla;
    }

    public String getFormadepago() {
        return formadepago;
    }

    public void setFormadepago(String formadepago) {
        this.formadepago = formadepago;
    }

    public double getImportetotal() {
        return importetotal;
    }

    public void setImportetotal(double importetotal) {
        this.importetotal = importetotal;
    }
    
}
