    void listarTotal(){
        String sql="select sum(importe) from registro_venta";
        try {
            cn=con.getConnection();
            st=cn.createStatement();
            rs=st.executeQuery(sql);
            
            while(rs.next()){
                double  total= rs.getDouble(1);
                
                txaR.append("\n");
                txaR.append("Total   :    " + total+"\n");
                }
            } catch (Exception e) {
            }
    }
    
