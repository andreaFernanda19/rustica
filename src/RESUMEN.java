    void listarResumenCliente(){
        String sql="select * from cliente";
        try {
            cn=con.getConnection();
            st=cn.createStatement();
            rs=st.executeQuery(sql);
            
            while(rs.next()){
                String  nombre= rs.getString(1);
                String  apellido= rs.getString(2);
                int dni = rs.getInt(3);
                
                txaR.append("Nombre   :    " + nombre+"\n");
                txaR.append("Apellido   :    "+apellido+"\n");
                txaR.append("DNI        :    "+dni+"\n");
                txaR.append("\n");
                }
            } catch (Exception e) {
            }
    }
    
